def program_landing_for(program: Program) -> list[dict]:
    """Роутер: выбирает набор блоков по program.category (с fallback по slug/name)."""
    cat = (program.category or "").lower()
    slug = (program.slug or "").lower()
    name_low = (program.name or "").lower()

    if cat == "school" or "shkole" in slug or "отличник" in name_low or "podgotov" in slug:
        return program_landing_school(program)
    if cat == "speech" or "logoped" in slug or "логопед" in name_low:
        return program_landing_speech(program)
    if cat == "english" or "anglij" in slug or "английск" in name_low:
        return program_landing_english(program)
    if cat == "early" or "rannee" in slug or "ранн" in name_low:
        return program_landing_early(program)
    return program_landing_generic(program)


